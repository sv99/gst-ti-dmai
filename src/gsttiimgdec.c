/******************************************************************************
 * gst_tiimgdec_init_image
 *     Initialize or re-initializes the image stream
 ******************************************************************************/
static gboolean gst_tiimgdec_init_image(GstTIImgdec *imgdec)
{
    Rendezvous_Attrs       rzvAttrs  = Rendezvous_Attrs_DEFAULT;
    struct sched_param     schedParam;
    pthread_attr_t         attr;
    Fifo_Attrs             fAttrs    = Fifo_Attrs_DEFAULT;

    GST_LOG("Begin\n");

    /* If image has already been initialized, shut down previous decoder */
    if (imgdec->hEngine) {
        if (!gst_tiimgdec_exit_image(imgdec)) {
            GST_ERROR("failed to shut down existing image decoder\n");
            return FALSE;
        }
    }

    /* Make sure we know what codec we're using */
    if (!imgdec->engineName) {
        GST_ERROR("engine name not specified\n");
        return FALSE;
    }

    if (!imgdec->codecName) {
        GST_ERROR("codec name not specified\n");
        return FALSE;
    }

    /* Set up the queue fifo */
    imgdec->hInFifo = Fifo_create(&fAttrs);

    /* Initialize thread status management */
    imgdec->threadStatus = 0UL;
    pthread_mutex_init(&imgdec->threadStatusMutex, NULL);

    /* Initialize rendezvous objects for making threads wait on conditions */
    imgdec->waitOnDecodeDrain  = Rendezvous_create(100, &rzvAttrs);
    imgdec->waitOnQueueThread  = Rendezvous_create(100, &rzvAttrs);
    imgdec->waitOnDecodeThread = Rendezvous_create(2, &rzvAttrs);
    imgdec->waitOnBufTab       = Rendezvous_create(100, &rzvAttrs);
    imgdec->drainingEOS        = FALSE;

    /* Initialize custom thread attributes */
    if (pthread_attr_init(&attr)) {
        GST_WARNING("failed to initialize thread attrs\n");
        gst_tiimgdec_exit_image(imgdec);
        return FALSE;
    }

    /* Force the thread to use the system scope */
    if (pthread_attr_setscope(&attr, PTHREAD_SCOPE_SYSTEM)) {
        GST_WARNING("failed to set scope attribute\n");
        gst_tiimgdec_exit_image(imgdec);
        return FALSE;
    }

    /* Force the thread to use custom scheduling attributes */
    if (pthread_attr_setinheritsched(&attr, PTHREAD_EXPLICIT_SCHED)) {
        GST_WARNING("failed to set schedule inheritance attribute\n");
        gst_tiimgdec_exit_image(imgdec);
        return FALSE;
    }

    /* Set the thread to be fifo real time scheduled */
    if (pthread_attr_setschedpolicy(&attr, SCHED_FIFO)) {
        GST_WARNING("failed to set FIFO scheduling policy\n");
        gst_tiimgdec_exit_image(imgdec);
        return FALSE;
    }

    /* Set the display thread priority */
    schedParam.sched_priority = GstTIVideoThreadPriority;
    if (pthread_attr_setschedparam(&attr, &schedParam)) {
        GST_WARNING("failed to set scheduler parameters\n");
        return FALSE;
    }

    /* Create decoder thread */
    if (pthread_create(&imgdec->decodeThread, &attr,
            gst_tiimgdec_decode_thread, (void*)imgdec)) {
        GST_ERROR("failed to create decode thread\n");
        gst_tiimgdec_exit_image(imgdec);
        return FALSE;
    }
    gst_tithread_set_status(imgdec, TIThread_DECODE_CREATED);

    /* Wait for decoder thread to finish initilization before creating queue
     * thread.
     */
    Rendezvous_meet(imgdec->waitOnDecodeThread);

    /* Make sure circular buffer and display buffer handles are created by
     * decoder thread.
     */
    if (imgdec->circBuf == NULL || imgdec->hOutBufTab == NULL) {
        GST_ERROR("decode thread failed to create circbuf or display buffer"
                  " handles\n");
        return FALSE;
    }

    /* Create queue thread */
    if (pthread_create(&imgdec->queueThread, NULL,
            gst_tiimgdec_queue_thread, (void*)imgdec)) {
        GST_ERROR("failed to create queue thread\n");
        gst_tiimgdec_exit_image(imgdec);
        return FALSE;
    }
    gst_tithread_set_status(imgdec, TIThread_QUEUE_CREATED);


    GST_LOG("Finish\n");
    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec_exit_image
 *    Shut down any running image decoder, and reset the element state.
 ******************************************************************************/
static gboolean gst_tiimgdec_exit_image(GstTIImgdec *imgdec)
{
    void*    thread_ret;
    gboolean checkResult;

    GST_LOG("Begin\n");

    /* Drain the pipeline if it hasn't already been drained */
    if (!imgdec->drainingEOS) {
       gst_tiimgdec_drain_pipeline(imgdec);
     }

    /* Shut down the decode thread */
    if (gst_tithread_check_status(
            imgdec, TIThread_DECODE_CREATED, checkResult)) {
        GST_LOG("shutting down decode thread\n");

        if (pthread_join(imgdec->decodeThread, &thread_ret) == 0) {
            if (thread_ret == GstTIThreadFailure) {
                GST_DEBUG("decode thread exited with an error condition\n");
            }
        }
    }

    /* Shut down the queue thread */
    if (gst_tithread_check_status(
            imgdec, TIThread_QUEUE_CREATED, checkResult)) {
        GST_LOG("shutting down queue thread\n");

        /* Unstop the queue thread if needed, and wait for it to finish */
        /* Push the gst_ti_flush_fifo buffer to let the queue thread know
         * when the Fifo has finished draining.  If the Fifo is currently
         * empty when we get to this point, then pushing this buffer will
         * also unblock the encode/decode thread if it is currently blocked
         * on a Fifo_get().  Our first thought was to use DMAI's Fifo_flush()
         * routine here, but this method assumes the Fifo to be empty and
         * will leak any buffer still in the Fifo.
         */
        if (Fifo_put(imgdec->hInFifo,&gst_ti_flush_fifo) < 0) {
            GST_ERROR("Could not put flush value to Fifo\n");
        }

        if (pthread_join(imgdec->queueThread, &thread_ret) == 0) {
            if (thread_ret == GstTIThreadFailure) {
                GST_DEBUG("queue thread exited with an error condition\n");
            }
        }
    }

    if (imgdec->hInFifo) {
        Fifo_delete(imgdec->hInFifo);
        imgdec->hInFifo = NULL;
    }

    if (imgdec->waitOnQueueThread) {
        Rendezvous_delete(imgdec->waitOnQueueThread);
        imgdec->waitOnQueueThread = NULL;
    }

    if (imgdec->waitOnDecodeDrain) {
        Rendezvous_delete(imgdec->waitOnDecodeDrain);
        imgdec->waitOnDecodeDrain = NULL;
    }

    if (imgdec->waitOnDecodeThread) {
        Rendezvous_delete(imgdec->waitOnDecodeThread);
        imgdec->waitOnDecodeThread = NULL;
    }

    if (imgdec->waitOnBufTab) {
        Rendezvous_delete(imgdec->waitOnBufTab);
        imgdec->waitOnBufTab = NULL;
    }

    /* Shut down thread status management */
    imgdec->threadStatus = 0UL;
    pthread_mutex_destroy(&imgdec->threadStatusMutex);

    /* Shut down remaining items */
    if (imgdec->circBuf) {
        GST_LOG("freeing cicrular input buffer\n");
        gst_ticircbuffer_unref(imgdec->circBuf);
        imgdec->circBuf      = NULL;
        imgdec->framerateNum = 0;
        imgdec->framerateDen = 0;
    }

    if (imgdec->hOutBufTab) {
        GST_LOG("freeing output buffers\n");
        BufTab_delete(imgdec->hOutBufTab);
        imgdec->hOutBufTab = NULL;
    }

    GST_LOG("Finish\n");
    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec_change_state
 *     Manage state changes for the image stream.  The gStreamer documentation
 *     states that state changes must be handled in this manner:
 *        1) Handle ramp-up states
 *        2) Pass state change to base class
 *        3) Handle ramp-down states
 ******************************************************************************/
static GstStateChangeReturn gst_tiimgdec_change_state(GstElement *element,
                                GstStateChange transition)
{
    GstStateChangeReturn  ret    = GST_STATE_CHANGE_SUCCESS;
    GstTIImgdec          *imgdec = GST_TIIMGDEC(element);

    GST_LOG("Begin change_state (%d)\n", transition);

    /* Handle ramp-up state changes */
    switch (transition) {
        case GST_STATE_CHANGE_NULL_TO_READY:
            break;
        default:
            break;
    }

    /* Pass state changes to base class */
    ret = GST_ELEMENT_CLASS(parent_class)->change_state(element, transition);
    if (ret == GST_STATE_CHANGE_FAILURE)
        return ret;

    /* Handle ramp-down state changes */
    switch (transition) {
        case GST_STATE_CHANGE_READY_TO_NULL:
            GST_LOG("State changed from READY to NULL.  Shutting down any"
                    "running image decoders\n");
            /* Shut down any running image decoder */
            if (!gst_tiimgdec_exit_image(imgdec)) {
                return GST_STATE_CHANGE_FAILURE;
            }
            break;

        default:
            break;
    }

    GST_LOG("Finish change_state\n");
    return ret;
}

/******************************************************************************
 * gst_tiimgdec_codec_stop
 *     Stop codec engine
 *****************************************************************************/
static gboolean gst_tiimgdec_codec_stop (GstTIImgdec  *imgdec)
{
    if (imgdec->hIe) {
        GST_LOG("closing image decoder\n");
        Idec_delete(imgdec->hIe);
        imgdec->hIe = NULL;
    }

    if (imgdec->hEngine) {
        GST_LOG("closing codec engine\n");
        Engine_close(imgdec->hEngine);
        imgdec->hEngine = NULL;
    }

    return TRUE;
}


/******************************************************************************
 * gst_tiimgdec_codec_start
 *     Initialize codec engine
 *****************************************************************************/
static gboolean gst_tiimgdec_codec_start (GstTIImgdec  *imgdec)
{
    BufferGfx_Attrs        gfxAttrs  = BufferGfx_Attrs_DEFAULT;

    /* Open the codec engine */
    GST_LOG("opening codec engine \"%s\"\n", imgdec->engineName);
    imgdec->hEngine = Engine_open((Char *) imgdec->engineName, NULL, NULL);

    if (imgdec->hEngine == NULL) {
        GST_ERROR("failed to open codec engine \"%s\"\n", imgdec->engineName);
        return FALSE;
    }
    GST_LOG("opened codec engine \"%s\" successfully\n", imgdec->engineName);

    if (!gst_tiimgdec_set_codec_attrs(imgdec)) {
        GST_ERROR("Error while trying to set the codec attrs\n");
        return FALSE;
    }

    GST_LOG("opening image decoder \"%s\"\n", imgdec->codecName);
    imgdec->hIe = Idec_create(imgdec->hEngine, (Char*)imgdec->codecName,
                      &imgdec->params, &imgdec->dynParams);

    if (imgdec->hIe == NULL) {
        GST_ERROR("failed to create image decoder: %s\n", imgdec->codecName);
        GST_DEBUG("Verify that the values being used for input and output ColorSpace are supported by your coded.\n");
        GST_LOG("closing codec engine\n");
        return FALSE;
    }

    /* Create a circular input buffer */
    imgdec->circBuf = gst_ticircbuffer_new(
                           Idec_getInBufSize(imgdec->hIe), 3, FALSE);

    if (imgdec->circBuf == NULL) {
        GST_ERROR("failed to create circular input buffer\n");
        return FALSE;
    }

    /* Display buffer contents if displayBuffer=TRUE was specified */
    gst_ticircbuffer_set_display(imgdec->circBuf, imgdec->displayBuffer);

    /* Define the number of display buffers to allocate.  This number must be
     * at least 1, but should be more if codecs don't return a display buffer
     * after every process call.  If this has not been set via set_property(),
     * default to the value set above based on device type.
     */
    if (imgdec->numOutputBufs == 0) {
        imgdec->numOutputBufs = 1;
    }

    /* Create codec output buffers */
    GST_LOG("creating output buffer table\n");
    gfxAttrs.colorSpace     = gst_tiimgdec_codec_color_space_to_dmai(imgdec->params.forceChromaFormat);
    gfxAttrs.dim.width      = imgdec->params.maxWidth;
    gfxAttrs.dim.height     = imgdec->params.maxHeight;
    gfxAttrs.dim.lineLength = BufferGfx_calcLineLength(
                                  gfxAttrs.dim.width, gfxAttrs.colorSpace);

    gfxAttrs.bAttrs.memParams.align = 128;
    /* Both the codec and the GStreamer pipeline can own a buffer */
    gfxAttrs.bAttrs.useMask = gst_tidmaibuffertransport_GST_FREE |
                              gst_tiimgdec_CODEC_FREE;

    imgdec->hOutBufTab =
        BufTab_create(imgdec->numOutputBufs, Idec_getOutBufSize(imgdec->hIe),
            BufferGfx_getBufferAttrs(&gfxAttrs));

    if (imgdec->hOutBufTab == NULL) {
        GST_ERROR("failed to create output buffers\n");
        return FALSE;
    }

    return TRUE;
}

/******************************************************************************
 * gst_tiimgdec_decode_thread
 *     Call the image codec to process a full input buffer
 ******************************************************************************/
static void* gst_tiimgdec_decode_thread(void *arg)
{
    GstTIImgdec           *imgdec        = GST_TIIMGDEC(gst_object_ref(arg));
    GstBuffer              *encDataWindow  = NULL;
    gboolean               codecFlushed    = FALSE;
    void                   *threadRet      = GstTIThreadSuccess;
    BufferGfx_Attrs        gfxAttrs        = BufferGfx_Attrs_DEFAULT;
    Buffer_Handle          hDstBuf;
    Int32                  encDataConsumed;
    GstClockTime           encDataTime;
    GstClockTime           frameDuration;
    Buffer_Handle          hEncDataWindow;
    BufferGfx_Dimensions   dim;
    GstBuffer              *outBuf;
    Int                    ret;


    GST_LOG("Begin\n");
    /* Calculate the duration of a single frame in this stream */
    frameDuration = gst_tiimgdec_frame_duration(imgdec);

    /* Initialize codec engine */
    ret = gst_tiimgdec_codec_start(imgdec);

    /* Notify main thread if it is waiting to create queue thread */
    Rendezvous_meet(imgdec->waitOnDecodeThread);

    if (ret == FALSE) {
        GST_ERROR("failed to start codec\n");
        goto thread_exit;
    }

    /* Main thread loop */
    while (TRUE) {

        /* Obtain an encoded data frame */
        encDataWindow  = gst_ticircbuffer_get_data(imgdec->circBuf);
        encDataTime    = GST_BUFFER_TIMESTAMP(encDataWindow);
        hEncDataWindow = GST_TIDMAIBUFFERTRANSPORT_DMAIBUF(encDataWindow);

        /* If we received a data frame of zero size, there is no more data to
         * process -- exit the thread.  If we weren't told that we are
         * draining the pipeline, something is not right, so exit with an
         * error.
         */
        if (GST_BUFFER_SIZE(encDataWindow) == 0) {
            GST_LOG("no image data remains\n");
            if (!imgdec->drainingEOS) {
                goto thread_failure;
            }

            goto thread_exit;
        }

        /* Obtain a free output buffer for the decoded data */
        /* If we are not able to find free buffer from BufTab then decoder 
         * thread will be blocked on waitOnBufTab rendezvous. And this will be 
         * woke-up by dmaitransportbuffer finalize method.
         */
        hDstBuf = BufTab_getFreeBuf(imgdec->hOutBufTab);
        if (hDstBuf == NULL) {
            Rendezvous_meet(imgdec->waitOnBufTab);
            hDstBuf = BufTab_getFreeBuf(imgdec->hOutBufTab);

            if (hDstBuf == NULL) {
                GST_ERROR("failed to get a free contiguous buffer from"
                            " BufTab\n");
                goto thread_failure;
            }
        }

        /* Reset waitOnBufTab rendezvous handle to its orignal state */
        Rendezvous_reset(imgdec->waitOnBufTab);

        /* Make sure the whole buffer is used for output */
        BufferGfx_resetDimensions(hDstBuf);

        /* Create a BufferGfx object that will point to a reference buffer from
         * The circular buffer.  This is needed for the decoder which requires
         * that the input buffer be a BufferGfx object.
         */
        BufferGfx_getBufferAttrs(&gfxAttrs)->reference = TRUE;
        imgdec->hInBuf = Buffer_create(Buffer_getSize(hEncDataWindow),
                                BufferGfx_getBufferAttrs(&gfxAttrs));
        Buffer_setUserPtr(imgdec->hInBuf, Buffer_getUserPtr(hEncDataWindow));
        Buffer_setNumBytesUsed(imgdec->hInBuf, 
                               Buffer_getSize(imgdec->hInBuf));

        /* Set the dimensions of the buffer to the resolution */
        BufferGfx_getDimensions(hDstBuf, &dim);
        dim.width = imgdec->params.maxWidth;
        dim.height = imgdec->params.maxHeight;
        BufferGfx_setColorSpace(imgdec->hInBuf,
                     ColorSpace_UYVY); 
        dim.lineLength = BufferGfx_calcLineLength(imgdec->params.maxWidth,
                                  gfxAttrs.colorSpace);

        BufferGfx_setDimensions(hDstBuf, &dim);
        Buffer_setNumBytesUsed(hDstBuf, Buffer_getSize(hDstBuf));

        /* Invoke the image decoder */
        GST_LOG("invoking the image decoder\n");
        ret             = Idec_process(imgdec->hIe, imgdec->hInBuf, hDstBuf);
        encDataConsumed = (codecFlushed) ? 0 :
                          Buffer_getNumBytesUsed(hEncDataWindow);

        /* Delete the temporary Graphics buffer used for the decode process */
        Buffer_delete(imgdec->hInBuf);

        if (ret < 0) {
            GST_ERROR("failed to decode image buffer\n");
            goto thread_failure;
        }

        /* If no data was used we must have some kind of decode error */
        if (ret == Dmai_EBITERROR && encDataConsumed == 0 && !codecFlushed) {
            GST_ERROR("fatal bit error\n");
            goto thread_failure;
        }

        if (ret > 0) {
            GST_LOG("Idec_process returned success code %d\n", ret); 
        }

        /* Release the referdece buffer, and tell the circular buffer how much
         * data was consumed.
         */
        ret = gst_ticircbuffer_data_consumed(imgdec->circBuf, encDataWindow,
                  encDataConsumed);
        encDataWindow = NULL;

        if (!ret) {
            goto thread_failure;
        }


        /* Set the source pad capabilities based on the decoded frame
         * properties.
         */
        gst_tiimgdec_set_source_caps(imgdec, hDstBuf);

        /* Create a DMAI transport buffer object to carry a DMAI buffer to
         * the source pad.  The transport buffer knows how to release the
         * buffer for re-use in this element when the source pad calls
         * gst_buffer_unref().
         */
        outBuf = gst_tidmaibuffertransport_new(hDstBuf, imgdec->waitOnBufTab);
        gst_buffer_set_data(outBuf, GST_BUFFER_DATA(outBuf),
             gst_ti_calculate_display_bufSize(hDstBuf));
        gst_buffer_set_caps(outBuf, GST_PAD_CAPS(imgdec->srcpad));

        /* If we have a valid time stamp, set it on the buffer */
        if (imgdec->genTimeStamps &&
            GST_CLOCK_TIME_IS_VALID(encDataTime)) {
            GST_LOG("image timestamp value: %llu\n", encDataTime);
            GST_BUFFER_TIMESTAMP(outBuf) = encDataTime;
            GST_BUFFER_DURATION(outBuf)  = frameDuration;
        }
        else {
            GST_BUFFER_TIMESTAMP(outBuf) = GST_CLOCK_TIME_NONE;
        }

        /* Tell circular buffer how much time we consumed */
        gst_ticircbuffer_time_consumed(imgdec->circBuf, frameDuration);

        /* Push the transport buffer to the source pad */
        GST_LOG("pushing display buffer to source pad\n");

        if (gst_pad_push(imgdec->srcpad, outBuf) != GST_FLOW_OK) {
            GST_DEBUG("push to source pad failed\n");
            goto thread_failure;
        }

        /* Release buffers no longer in use by the codec */
        Buffer_freeUseMask(hDstBuf, gst_tiimgdec_CODEC_FREE);
    }

thread_failure:

    /* If decDataWindow is non-NULL, something bad happened before we had a
     * chance to release it.  Release it now so we don't block the pipeline.
     * We release it by telling the circular buffer that we're done with it and
     * consumed no data.
     */
    if (encDataWindow) {
        gst_ticircbuffer_data_consumed(imgdec->circBuf, encDataWindow, 0);
    }

    gst_tithread_set_status(imgdec, TIThread_DECODE_ABORTED);
    threadRet = GstTIThreadFailure;
    gst_ticircbuffer_consumer_aborted(imgdec->circBuf);
    Rendezvous_force(imgdec->waitOnQueueThread);

thread_exit:
 
    /* Stop codec engine */
    if (gst_tiimgdec_codec_stop(imgdec) < 0) {
        GST_ERROR("failed to stop codec\n");
    }

    imgdec->decodeDrained = TRUE;
    Rendezvous_force(imgdec->waitOnDecodeDrain);

    gst_object_unref(imgdec);

    GST_LOG("Finish image decode_thread (%d)\n", (int)threadRet);
    return threadRet;
}


/******************************************************************************
 * gst_tiimgdec_queue_thread 
 *     Add an input buffer to the circular buffer            
 ******************************************************************************/
static void* gst_tiimgdec_queue_thread(void *arg)
{
    GstTIImgdec* imgdec    = GST_TIIMGDEC(gst_object_ref(arg));
    void*        threadRet = GstTIThreadSuccess;
    GstBuffer*   decData;
    Int          fifoRet;

    GST_LOG("Begin\n");
    while (TRUE) {

        /* Get the next input buffer (or block until one is ready) */
        fifoRet = Fifo_get(imgdec->hInFifo, &decData);

        if (fifoRet < 0) {
            GST_ERROR("Failed to get buffer from input fifo\n");
            goto thread_failure;
        }

        if (decData == (GstBuffer *)(&gst_ti_flush_fifo)) {
            GST_DEBUG("Processed last input buffer from Fifo; exiting.\n");
            goto thread_exit;
        }

/* This code is if'ed out for now until more work has been done for state
 * transitions.  For now we do not want to print this message repeatedly
 * which will happen when flushing the fifo when the decode thread has
 * exited.
 */
        /* Send the buffer to the circular buffer */
        if (!gst_ticircbuffer_queue_data(imgdec->circBuf, decData)) {
#if 0
            GST_ERROR("queue thread could not queue data\n");
            GST_ERROR("queue thread encData size = %d\n", GST_BUFFER_SIZE(encData));
            gst_buffer_unref(encData);
            goto thread_failure;
#else
            ; /* Do nothing */
#endif
        }

        /* Release the buffer we received from the sink pad */
        gst_buffer_unref(decData);

        /* If we've reached the EOS, start draining the circular buffer when
         * there are no more buffers in the FIFO.
         */
        if (imgdec->drainingEOS && 
            Fifo_getNumEntries(imgdec->hInFifo) == 0) {
            gst_ticircbuffer_drain(imgdec->circBuf, TRUE);
        }

        /* Unblock any pending puts to our Fifo if we have reached our
         * minimum threshold.
         */
        gst_tiimgdec_broadcast_queue_thread(imgdec);
    }

thread_failure:
    gst_tithread_set_status(imgdec, TIThread_QUEUE_ABORTED);
    threadRet = GstTIThreadFailure;

thread_exit:
    gst_object_unref(imgdec);
    GST_LOG("Finish\n");
    return threadRet;
}


/******************************************************************************
 * gst_tiimgdec_wait_on_queue_thread
 *    Wait for the queue thread to consume buffers from the fifo until
 *    there are only "waitQueueSize" items remaining.
 ******************************************************************************/
static void gst_tiimgdec_wait_on_queue_thread(GstTIImgdec *imgdec,
                Int32 waitQueueSize)
{
    GST_LOG("Begin\n");
    imgdec->waitQueueSize = waitQueueSize;
    Rendezvous_meet(imgdec->waitOnQueueThread);
    GST_LOG("Finish\n");
}


/******************************************************************************
 * gst_tiimgdec_broadcast_queue_thread
 *    Broadcast when queue thread has processed enough buffers from the
 *    fifo to unblock anyone waiting to queue some more.
 ******************************************************************************/
static void gst_tiimgdec_broadcast_queue_thread(GstTIImgdec *imgdec)
{
    GST_LOG("Begin\n");
    if (imgdec->waitQueueSize < Fifo_getNumEntries(imgdec->hInFifo)) {
          return;
    } 
    Rendezvous_force(imgdec->waitOnQueueThread);
    GST_LOG("Finish\n");
}


/******************************************************************************
 * gst_tiimgdec_drain_pipeline
 *    Push any remaining input buffers through the queue and decode threads
 ******************************************************************************/
static void gst_tiimgdec_drain_pipeline(GstTIImgdec *imgdec)
{
    gboolean checkResult;

    GST_LOG("Begin\n");
    imgdec->drainingEOS = TRUE;

    /* If the processing threads haven't been created, there is nothing to
     * drain.
     */
    if (!gst_tithread_check_status(
             imgdec, TIThread_DECODE_CREATED, checkResult)) {
        return;
    }
    if (!gst_tithread_check_status(
             imgdec, TIThread_QUEUE_CREATED, checkResult)) {
        return;
    }

    /* If the queue fifo still has entries in it, it will drain the
     * circular buffer once all input buffers have been added to the
     * circular buffer.  If the fifo is already empty, we must drain
     * the circular buffer here.
     */
    if (Fifo_getNumEntries(imgdec->hInFifo) == 0) {
        gst_ticircbuffer_drain(imgdec->circBuf, TRUE);
    }
    else {
        Rendezvous_force(imgdec->waitOnQueueThread);
    }

    /* Wait for the decoder to drain */
    if (!imgdec->decodeDrained) {
        Rendezvous_meet(imgdec->waitOnDecodeDrain);
    }
    imgdec->decodeDrained = FALSE;

    GST_LOG("Finish\n");
}


/******************************************************************************
 * gst_tiimgdec_frame_duration
 *    Return the duration of a single frame in nanoseconds.
 ******************************************************************************/
static GstClockTime gst_tiimgdec_frame_duration(GstTIImgdec *imgdec)
{
    GST_LOG("Begin\n");
    /* Default to 29.97 if the frame rate was not specified */
    if (imgdec->framerateNum == 0 && imgdec->framerateDen == 0) {
        GST_DEBUG("framerate not specified; using 29.97fps");
        imgdec->framerateNum = 30000;
        imgdec->framerateDen = 1001;
    }

    GST_LOG("Finish\n");
    return (GstClockTime)
        ((1 / ((gdouble)imgdec->framerateNum/(gdouble)imgdec->framerateDen))
         * GST_SECOND);
}


