/******************************************************************************
 * gst_tiimgdec_init_env
 *  Initialize element property default by reading environment variables.
 *****************************************************************************/
static void gst_tiimgdec_init_env(GstTIImgdec *imgdec)
{
    GST_LOG("gst_tiimgdec_init_env - begin");

    if (gst_ti_env_is_defined("GST_TI_TIImgdec_engineName")) {
        imgdec->engineName = gst_ti_env_get_string("GST_TI_TIImgdec_engineName");
        GST_LOG("Setting engineName=%s\n", imgdec->engineName);
    }

    if (gst_ti_env_is_defined("GST_TI_TIImgdec_codecName")) {
        imgdec->codecName = gst_ti_env_get_string("GST_TI_TIImgdec_codecName");
        GST_LOG("Setting codecName=%s\n", imgdec->codecName);
    }
    
    if (gst_ti_env_is_defined("GST_TI_TIImgdec_numOutputBufs")) {
        imgdec->numOutputBufs = 
                            gst_ti_env_get_int("GST_TI_TIImgdec_numOutputBufs");
        GST_LOG("Setting numOutputBufs=%ld\n", imgdec->numOutputBufs);
    }
    
    if (gst_ti_env_is_defined("GST_TI_TIImgdec_displayBuffer")) {
        imgdec->displayBuffer = 
                gst_ti_env_get_boolean("GST_TI_TIImgdec_displayBuffer");
        GST_LOG("Setting displayBuffer=%s\n",
                 imgdec->displayBuffer  ? "TRUE" : "FALSE");
    }

   if (gst_ti_env_is_defined("GST_TI_TIImgdec_genTimeStamps")) {
        imgdec->genTimeStamps = 
                gst_ti_env_get_boolean("GST_TI_TIImgdec_genTimeStamps");
        GST_LOG("Setting genTimeStamps =%s\n", 
                    imgdec->genTimeStamps ? "TRUE" : "FALSE");
    }
    
    if (gst_ti_env_is_defined("GST_TI_TIImgdec_framerate")) {
        imgdec->framerateNum = gst_ti_env_get_int("GST_TI_TIImgdec_framerate");
        imgdec->framerateDen = 1;
        
       /* If 30fps was specified, use 29.97 */        
        if (imgdec->framerateNum == 30) {
            imgdec->framerateNum = 30000;
            imgdec->framerateDen = 1001;
        }
    }

    if (gst_ti_env_is_defined("GST_TI_TIImgdec_resolution")) {
        sscanf(gst_ti_env_get_string("GST_TI_TIImgdec_resolution"), "%dx%d", 
                &imgdec->width,&imgdec->height);
        GST_LOG("Setting resolution=%dx%d\n", imgdec->width, imgdec->height);
    }
    
    GST_LOG("gst_tiimgdec_init_env - end");
}

/******************************************************************************
 * gst_tiimgdec_init
 *    Initializes a new element instance, instantiates pads and sets the pad
 *    callback functions.
 ******************************************************************************/
static void gst_tiimgdec_init(GstTIImgdec *imgdec, GstTIImgdecClass *gclass)
{
    GST_LOG("Begin\n");
    /* Instantiate decoded image sink pad
     *
     * Fixate on our static template caps insteam of writing a getcaps
     * function which is overkill for this element.
     */
    imgdec->sinkpad =
        gst_pad_new_from_static_template(&sink_factory, "sink");
    gst_pad_set_setcaps_function(
        imgdec->sinkpad, GST_DEBUG_FUNCPTR(gst_tiimgdec_set_sink_caps));
//    gst_pad_set_getcaps_function(
//        imgdec->sinkpad, GST_DEBUG_FUNCPTR(gst_pad_proxy_getcaps));
    gst_pad_set_event_function(
        imgdec->sinkpad, GST_DEBUG_FUNCPTR(gst_tiimgdec_sink_event));
    gst_pad_set_chain_function(
        imgdec->sinkpad, GST_DEBUG_FUNCPTR(gst_tiimgdec_chain));
    gst_pad_fixate_caps(imgdec->sinkpad,
        gst_caps_make_writable(
            gst_caps_copy(gst_pad_get_pad_template_caps(imgdec->sinkpad))));

    /* Instantiate deceoded image source pad
     *
     * Fixate on our static template caps insteam of writing a getcaps
     * function which is overkill for this element.
     */
    imgdec->srcpad =
        gst_pad_new_from_static_template(&src_factory, "src");
//    gst_pad_set_getcaps_function(
//        imgdec->srcpad, GST_DEBUG_FUNCPTR(gst_pad_proxy_getcaps));
    gst_pad_fixate_caps(imgdec->srcpad,
        gst_caps_make_writable(
            gst_caps_copy(gst_pad_get_pad_template_caps(imgdec->srcpad))));

    /* Add pads to TIImgdec element */
    gst_element_add_pad(GST_ELEMENT(imgdec), imgdec->sinkpad);
    gst_element_add_pad(GST_ELEMENT(imgdec), imgdec->srcpad);

    /* Initialize TIImgdec state */
    imgdec->engineName         = NULL;
    imgdec->codecName          = NULL;
    imgdec->displayBuffer      = FALSE;
    imgdec->genTimeStamps      = FALSE;
    imgdec->width              = 0;
    imgdec->height             = 0;

    imgdec->hEngine            = NULL;
    imgdec->hIe                = NULL;
    imgdec->drainingEOS        = FALSE;
    imgdec->threadStatus       = 0UL;
    imgdec->capsSet            = FALSE;

    imgdec->decodeDrained      = FALSE;
    imgdec->waitOnDecodeDrain  = NULL;
    imgdec->waitOnBufTab       = NULL;

    imgdec->waitOnDecodeThread = NULL;

    imgdec->hInFifo            = NULL;

    imgdec->waitOnQueueThread  = NULL;
    imgdec->waitQueueSize      = 0;

    imgdec->framerateNum       = 0;
    imgdec->framerateDen       = 0;

    imgdec->numOutputBufs      = 0UL;
    imgdec->hOutBufTab         = NULL;
    imgdec->circBuf            = NULL;

    gst_tiimgdec_init_env(imgdec);

    GST_LOG("Finish\n");
}

/******************************************************************************
 * gst_tiimgdec_set_property
 *     Set element properties when requested.
 ******************************************************************************/
static void gst_tiimgdec_set_property(GObject *object, guint prop_id,
                const GValue *value, GParamSpec *pspec)
{
    GstTIImgdec *imgdec = GST_TIIMGDEC(object);

    GST_LOG("Begin\n");

    switch (prop_id) {
        case PROP_ENGINE_NAME:
            if (imgdec->engineName) {
                g_free((gpointer)imgdec->engineName);
            }
            imgdec->engineName =
                (gchar*)g_malloc(strlen(g_value_get_string(value)) + 1);
            strcpy((gchar *)imgdec->engineName, g_value_get_string(value));
            GST_LOG("setting \"engineName\" to \"%s\"\n", imgdec->engineName);
            break;
        case PROP_CODEC_NAME:
            if (imgdec->codecName) {
                g_free((gpointer)imgdec->codecName);
            }
            imgdec->codecName =
                (gchar*)g_malloc(strlen(g_value_get_string(value)) + 1);
            strcpy((gchar*)imgdec->codecName, g_value_get_string(value));
            GST_LOG("setting \"codecName\" to \"%s\"\n", imgdec->codecName);
            break;
        case PROP_RESOLUTION:
            sscanf(g_value_get_string(value), "%dx%d", &imgdec->width,
                    &imgdec->height);
            GST_LOG("setting \"resolution\" to \"%dx%d\"\n",
                imgdec->width, imgdec->height);
            break;
        case PROP_NUM_OUTPUT_BUFS:
            imgdec->numOutputBufs = g_value_get_int(value);
            GST_LOG("setting \"numOutputBufs\" to \"%ld\"\n",
                imgdec->numOutputBufs);
            break;
        case PROP_FRAMERATE:
        {
            imgdec->framerateNum = g_value_get_int(value);
            imgdec->framerateDen = 1;

            /* If 30fps was specified, use 29.97 */
            if (imgdec->framerateNum == 30) {
                imgdec->framerateNum = 30000;
                imgdec->framerateDen = 1001;
            }

            GST_LOG("setting \"frameRate\" to \"%2.2lf\"\n",
                (gdouble)imgdec->framerateNum /
                (gdouble)imgdec->framerateDen);
            break;
        }
        case PROP_DISPLAY_BUFFER:
            imgdec->displayBuffer = g_value_get_boolean(value);
            GST_LOG("setting \"displayBuffer\" to \"%s\"\n",
                imgdec->displayBuffer ? "TRUE" : "FALSE");
            break;
        case PROP_GEN_TIMESTAMPS:
            imgdec->genTimeStamps = g_value_get_boolean(value);
            GST_LOG("setting \"genTimeStamps\" to \"%s\"\n",
                imgdec->genTimeStamps ? "TRUE" : "FALSE");
            break;
        default:
            G_OBJECT_WARN_INVALID_PROPERTY_ID(object, prop_id, pspec);
            break;
    }

    GST_LOG("Finish\n");
}

