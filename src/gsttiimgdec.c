/*******************************************************************************
 * gst_tiimgdec_set_codec_attrs
 *     Set the attributes for the decode codec.  The general order is to give
 *     preferdece to values passed in by the user on the command line,
 *     then check the stream for the information.  If the required information
 *     cannot be determined by either method then error out.
 ******************************************************************************/
static gboolean gst_tiimgdec_set_codec_attrs(GstTIImgdec *imgdec)
{ 
    char *toColor;
    GST_LOG("Begin\n");

    /* Set Resolution 
     *
     * NOTE:  We assume that there is only 1 resolution.  i.e. there is
     *        no seperate value being used for input resolution and
     *        output resolution
     */
    imgdec->params.maxWidth = 
                    imgdec->width == 0 ?
                    imgdec->params.maxWidth :
                    imgdec->width;
    imgdec->params.maxHeight =
                    imgdec->height == 0 ?
                    imgdec->params.maxHeight :
                    imgdec->height;
    /* Set displayWidth to 0 in order to use decoded image width */
    imgdec->dynParams.displayWidth = 0;

    /* Check for valid values (NOTE: minimum width and height are 64) */
    if (imgdec->params.maxWidth < 64) {
        GST_ERROR("The resolution width (%ld) is too small.  Must be at least 64\n", imgdec->params.maxWidth);
        return FALSE;
    }
    if (imgdec->params.maxHeight < 64) {
        GST_ERROR("The resolution height (%ld) is too small.  Must be at least 64\n", imgdec->params.maxHeight);
        return FALSE;
    }

    /* Make the color human readable */
    toColor = gst_tiimgdec_codec_color_space_to_str(imgdec->params.forceChromaFormat);

    GST_DEBUG("\nCodec Parameters:\n"
              "\tparams.maxWidth = %ld\n"
              "\tparams.maxHeight = %ld\n"
              "\tparams.forceChromaFormat = %ld (%s)\n"
              "\tparams.maxScans - %ld\n"
              "\tparams.dataEndianness = %ld\n",
              imgdec->params.maxWidth, imgdec->params.maxHeight,
              imgdec->params.forceChromaFormat, toColor,
              imgdec->params.maxScans, imgdec->params.dataEndianness);
    GST_LOG("Finish\n");
    return TRUE;
}

